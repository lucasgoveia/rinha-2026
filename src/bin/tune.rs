// Offline threshold tuner for FAST_T constants in ivf.rs.
//
// Usage: tune <references.json.gz> <index.bin>
//
// Strategy:
//   1. Load references, treat every Nth vector as a "holdout query".
//   2. Treat the loaded index (built from all references) as ground truth via full
//      pass (nprobe=24).
//   3. Run fast pass (nprobe=5) on each holdout query.
//   4. Where fast and full disagree on fraud_count, record the worst_dist from
//      the fast pass — grouped by the fast_count value k.
//   5. Print quantiles per k. The minimum worst_dist among disagreements is a
//      safe lower bound for FAST_T[k] (escalate at-or-above this distance).

use api_lib::ivf;
use flate2::read::GzDecoder;
use serde::Deserialize;

#[derive(Deserialize)]
struct RefEntry {
    vector: [f32; 14],
    #[allow(dead_code)]
    label: String,
}

const SCALE: f32 = 10000.0;
const FAST_NPROBE: usize = 5;
const FULL_NPROBE: usize = 24;
const HOLDOUT_STRIDE: usize = 10; // sample every 10th vector

fn quantize(v: &[f32; 14]) -> [i16; 14] {
    v.map(|x| (x * SCALE).round().clamp(-SCALE, SCALE) as i16)
}

fn main() {
    let refs_path = std::env::args().nth(1)
        .unwrap_or_else(|| "/resources/references.json.gz".to_string());
    let index_path = std::env::args().nth(2)
        .unwrap_or_else(|| "/data/index.bin".to_string());

    eprintln!("[tune] loading references from {}", refs_path);
    let file = std::fs::File::open(&refs_path).expect("open refs");
    let gz = GzDecoder::new(file);
    let entries: Vec<RefEntry> = serde_json::from_reader(gz).expect("parse refs");
    let queries: Vec<[i16; 14]> = entries.iter()
        .step_by(HOLDOUT_STRIDE)
        .map(|e| quantize(&e.vector))
        .collect();
    eprintln!("[tune] {} holdout queries", queries.len());

    eprintln!("[tune] loading index from {}", index_path);
    let idx = ivf::IvfIndex::load(&index_path);

    // Per-fast-count, collect worst_dist values where fast disagrees with full.
    let mut disagree: [Vec<i64>; 6] = Default::default();
    let mut total_disagree = 0usize;
    let mut total = 0usize;

    for q in &queries {
        let (full_count, _) = idx.query_probe(q, FULL_NPROBE);
        let (fast_count, fast_worst) = idx.query_probe(q, FAST_NPROBE);
        total += 1;
        if fast_count != full_count && (fast_count as usize) < 6 {
            disagree[fast_count as usize].push(fast_worst);
            total_disagree += 1;
        }
    }

    eprintln!("[tune] {}/{} queries had fast/full disagreement", total_disagree, total);
    println!("// Generated by bin/tune");
    println!("// total queries: {}, disagreements: {}", total, total_disagree);
    println!("const FAST_T: [i64; 6] = [");
    for k in 0..6 {
        let mut ds = disagree[k].clone();
        ds.sort_unstable();
        let n = ds.len();
        if n == 0 {
            println!("    i64::MAX, // count={} — no disagreements observed", k);
            continue;
        }
        let p10 = ds[n / 10];
        let p50 = ds[n / 2];
        let p90 = ds[(n * 9) / 10];
        // Use p10 as conservative escalation threshold (escalate if worst_dist >= p10)
        println!("    {:>13}, // count={} n={} p10={} p50={} p90={}",
                 p10, k, n, p10, p50, p90);
    }
    println!("];");
}
